""" 
Object-Oriented Programming (OOP) concepts are fundamental principles in 
software development that allow for organizing and designing code using 
objects and classes. Here are the core OOP concepts:

Class: A blueprint for creating objects. It defines attributes (data) and 
methods (functions) that all instances of the class will have.

Object: An instance of a class. It represents a real-world entity and has 
its own state (attributes) and behavior (methods).

Encapsulation: Bundling data (attributes) and methods that operate on the 
data into a single unit (class). Encapsulation allows for data hiding, which 
means restricting direct access to some of an object's components.

Inheritance: A mechanism where one class (subclass or derived class) inherits 
the properties and behaviors of another class (superclass or base class). 
It promotes code reusability and establishes a hierarchy between classes.

Polymorphism: The ability of different classes to be treated as instances of the
same superclass. Polymorphism allows methods to be overridden in subclasses, 
enabling dynamic binding and runtime method resolution.

Abstraction: Representing essential features without including the background 
details. It focuses on what an object does rather than how it does it. Abstract 
classes and interfaces are used to achieve abstraction in programming languages.

Multiple Inheritance in Java:
Java supports single inheritance, meaning a class can only inherit from one 
superclass. This design choice was made to avoid complexities and ambiguities 
that can arise with multiple inheritance, such as the diamond problem 
(where ambiguity arises due to two classes inheriting from the same superclass).

However, Java does support interface inheritance, where a class can implement 
multiple interfaces. Interfaces in Java provide a way to achieve abstraction and
multiple inheritance of type (interface) without inheriting implementation details.
"""