#   When will the else part of try-except-else be executed?



""" 
try block: This block contains the code that might raise an exception.


except block: If an exception occurs in the try block, and it matches 
              the exception specified in the except block, the control 
              is transferred to the except block to handle the exception.


else block: This block executes only if the try block does not raise any 
            exceptions. It is typically used for code that should run if 
            no errors occur.


finally block (optional): This block, if present, executes regardless of 
                            whether an exception occurred or not, typically
                            used for cleanup actions.
        

"""