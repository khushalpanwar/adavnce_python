#  What is Instantiation in terms of OOP terminology?

""" 
In Object-Oriented Programming (OOP), instantiation refers to 
the process of creating an instance (object) of a class.

Key Points about Instantiation:
Class Definition: A class serves as a blueprint or template that 
defines the properties (attributes) and behaviors (methods) that 
objects of the class will have.

Instance (Object): An instance is a realization or concrete manifestation 
of a class. It represents a specific entity or object in the real world.

Instantiation Process:

When you instantiate a class, you create a new instance (object) of that class.

The process typically involves using the class name followed by parentheses () to 
call the class constructor, which initializes the object's attributes and prepares it for use.


The constructor method (__init__ method in Python) is automatically called during instantiation 
to initialize the object's state.

"""